import { useMemo, useState } from 'react';

function hue(color: string) {
  return new ColorTranslator(color).HSLObject.H;
}

function lum(color: string) {
  return new ColorTranslator(color).CIELabObject.L;
}

// function sat(color: string) {
//   return new ColorTranslator(color).HSLObject.S;
// }

function App() {
  const [hueStart, setHueStart] = useState(0);
  const [hueFrom, setHueFrom] = useState(-180);
  const [hueTo, setHueTo] = useState(180);

  // const [satFrom, setSatFrom] = useState(0);
  // const [satTo, setSatTo] = useState(100);
  const [lumFrom, setLumFrom] = useState(0);
  const [lumTo, setLumTo] = useState(100);

  const matchingHues = useMemo(() => {
    return _.filter(colorEntries, ([, val]) => {
      const h = (hue(val) - hueFrom - hueStart) % 360; // hue adjusted so that `hueFrom` is the origin
      return 0 <= h && h < hueTo - hueFrom;
    });
  }, [colorEntries, hueFrom, hueTo, hueStart]);

  // const matchingSats = useMemo(
  //   () =>
  //     _.filter(matchingHues, ([, val]) => {
  //       const s = sat(val);
  //       return satFrom <= s && s <= satTo;
  //     }),
  //   [matchingHues, satFrom, satTo]
  // );

  const matchingLums = useMemo(
    () =>
      _.filter(matchingHues, ([, val]) => {
        const s = lum(val);
        return lumFrom <= s && s <= lumTo;
      }),
    [matchingHues, lumFrom, lumTo]
  );

  const matching = matchingLums;

  console.log({ range: hueTo - hueFrom, unit: (hueTo - hueFrom) / 3 });
  const groups = _.groupBy(matching, ([, val]) => {
    const h = (hue(val) - hueFrom - hueStart) % 360;
    if (matching.length < 40) {
      console.log({ h });
    }
    const unit = (hueTo - hueFrom) / 3;
    return Math.floor(h / unit);
  });

  return (
    <Container fixed>
      <Stack spacing={2}>
        <Paper>
          <FormGroup
            sx={{
              '& .MuiSlider-track': { background: 'none' },
            }}
          >
            <FormControlLabel
              control={
                <Slider
                  min={0}
                  max={360}
                  value={hueStart}
                  valueLabelDisplay="auto"
                  onChange={(_, val) => setHueStart(val as number)}
                  sx={{
                    '& .MuiSlider-rail': {
                      background:
                        'linear-gradient(to right, hsl(0, 100%, 50%), hsl(90, 100%, 50%), hsl(180, 100%, 50%),hsl(270, 100%, 50%),hsl(360, 100%, 50%))',
                    },
                  }}
                />
              }
              label="Middle Hue"
              labelPlacement="top"
            />
            <hr />
            <FormControlLabel
              control={
                <Slider
                  min={-180}
                  max={180}
                  valueLabelDisplay="auto"
                  value={[hueFrom, hueTo]}
                  onChange={(_, range) => {
                    const [from, to] = range as [number, number];
                    setHueFrom(from);
                    setHueTo(to);
                  }}
                  sx={{
                    '& .MuiSlider-rail': {
                      background: `linear-gradient(to right, ${[
                        0, 1, 2, 3, 4,
                      ].map(
                        (idx) => `hsl(${hueStart + idx * 90 - 180}, 100%, 50%)`
                      )})`,
                    },
                  }}
                />
              }
              label="Hue"
              labelPlacement="top"
            />
            <hr />
            {/* <FormControlLabel
              control={
                <Slider
                  min={0}
                  max={100}
                  valueLabelDisplay="auto"
                  value={[satFrom, satTo]}
                  onChange={(_, range) => {
                    const [from, to] = range as [number, number];
                    setSatFrom(from);
                    setSatTo(to);
                  }}
                  sx={{
                    '& .MuiSlider-rail': {
                      background: `linear-gradient(to right, hsl(${hueStart}, 0%, 100%), hsl(${hueStart}, 100%, 50%))`,
                    },
                  }}
                />
              }
              label="Saturation"
              labelPlacement="top"
            />
            <hr /> */}
            <FormControlLabel
              control={
                <Slider
                  min={0}
                  max={100}
                  valueLabelDisplay="auto"
                  value={[lumFrom, lumTo]}
                  onChange={(_, range) => {
                    const [from, to] = range as [number, number];
                    setLumFrom(from);
                    setLumTo(to);
                  }}
                  sx={{
                    '& .MuiSlider-rail': {
                      background: `linear-gradient(to right, hsl(${hueStart}, 100%, 0%), hsl(${hueStart}, 100%, 50%))`,
                    },
                  }}
                />
              }
              label="Luminance"
              labelPlacement="top"
            />
            <hr />
            <Stack direction="row">
              <Button
                onClick={() => {
                  setHueStart(0);
                  setHueFrom(-180);
                  setHueTo(180);
                  // setSatFrom(0);
                  // setSatTo(100);
                  setLumFrom(0);
                  setLumTo(100);
                }}
              >
                Reset
              </Button>
            </Stack>
          </FormGroup>
        </Paper>
        <Paper>
          <Stack direction="row" spacing={2} sx={{ overflowX: 'auto' }}>
            {Object.values(groups).map((grp) => (
              <Stack spacing={0.5}>
                {_.sortBy(grp, ([, val]) => -lum(val)).map(([name, val]) => (
                  <Card sx={{ backgroundColor: val }}>
                    <CardContent>
                      <Typography variant="h5">{name}</Typography>
                      <Typography variant="h6">{val}</Typography>
                      <Typography variant="body2">
                        H: {Math.round(hue(val))}
                        <br />
                        {/* S: {Math.round(sat(val))} */}
                        {/* <br /> */}
                        L: {Math.round(lum(val))}
                      </Typography>
                    </CardContent>
                  </Card>
                ))}
              </Stack>
            ))}
          </Stack>
        </Paper>
      </Stack>
    </Container>
  );
}

export default App;
